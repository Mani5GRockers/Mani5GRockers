// üîÅ LIVE AUTO-RANK ENGINE
function simulateLiveTraffic() {

  // 1Ô∏è‚É£ Update metrics + calculate score
  data.forEach(item => {

    const pingVar = Math.floor(Math.random() * 6) - 2;
    item.currentPing = Math.max(1, item.basePing + pingVar);

    const timeVar = (Math.random() * 0.04) - 0.02;
    item.currentTime = Math.max(0.01, +(item.baseTime + timeVar).toFixed(2));

    item.score = calculateScore(item, item.currentTime, item.currentPing);
  });

  // 2Ô∏è‚É£ Sort (LOWEST SCORE = BEST)
  data.sort((a, b) => a.score - b.score);

  // 3Ô∏è‚É£ Rebuild UI
  list.innerHTML = "";

  data.forEach((item, index) => {

    const row = document.createElement("div");
    row.className = `row ${index === 0 ? "winner" : ""}`;

    const rankDisplay = index === 0 ? "üèÜ" : index + 1;

    row.innerHTML = `
      <div class="rank">${rankDisplay}</div>

      <div class="name">
        <a href="${item.url}" target="_blank" rel="noopener nofollow">
          ${item.company}
        </a>
        <div class="desc">${item.desc}</div>
      </div>

      <div class="bar-col">
        <div class="bar-container">
          <div class="bar-fill"
               style="width:${(item.currentTime / maxTime) * 100}%">
          </div>
        </div>
      </div>

      <div class="metric load-val">${item.currentTime}s</div>
      <div class="metric uptime">${item.uptime}</div>

      <div class="metric">
        <span class="ping-val">${item.currentPing}</span>ms
        <div class="ping-dot"></div>
      </div>
    `;

    list.appendChild(row);
  });
}

